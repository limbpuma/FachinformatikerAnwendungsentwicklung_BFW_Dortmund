package aufgaben;

public class Aufgabe2 {

	public static void main(String[] args) {

		int april = 12, mai = 14, juni = 8;

		double durschnitt = (april + mai + juni) / 3.0;

		System.out.println("Niederschlag im April: " + april
				+ "\nNiederschlag im Mai:" + mai + "\nNiederschlag im Juni: "
				+ juni + "\nDurchschnitt: " + durschnitt);
	}

}